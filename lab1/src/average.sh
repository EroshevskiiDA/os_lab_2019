#!/bin/bash

# Инициализация счетчика и суммы
count=0
sum=0

# Цикл по всем входным аргументам
for arg in "$@"; do
 # Проверяем, является ли аргумент числом
 if [[ "$arg" =~ ^-?[0-9]+([.][0-9]+)?$ ]]; then
  # Увеличиваем счетчик
  count=$((count + 1))
  # Добавляем число к сумме
  sum=$((sum + arg))
 else
  echo "Ошибка: '$arg' не является числом."
 fi
done

# Выводим количество и среднее арифметическое
if [[ $count -gt 0 ]]; then
 average=$(echo "scale=2; $sum / $count" | bc)
 echo "Количество аргументов: $count"
 echo "Среднее арифметическое: $average"
fi
